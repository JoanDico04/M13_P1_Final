    private void eliminarEquipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarEquipActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow != -1){
            

           
            try {
                gbd.eliminarEquip(equips.get(selectedRow).getIdEquip());
                gbd.confirmarCanvis();

                equips.remove(selectedRow);

                omplirTaula();
            } catch (GestorBDException ex) {
                java.util.logging.Logger.getLogger(GestioEquip.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
               

            

        }
    }//GEN-LAST:event_eliminarEquipActionPerformed

