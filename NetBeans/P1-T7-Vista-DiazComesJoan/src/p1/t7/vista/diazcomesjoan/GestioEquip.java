    private void omplirTaula() {
        
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("Nom");
        model.addColumn("Tipus");

        
        
        for(Equip e : equips){
            model.addRow(new Object[]{
                e.getNomEquip(), 
                e.getEquipTipus(),
                });
        }
        
        jTable1.setModel(model);
    }

