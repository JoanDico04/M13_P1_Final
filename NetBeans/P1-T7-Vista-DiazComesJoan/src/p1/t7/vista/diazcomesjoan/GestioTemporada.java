    private void eliminarTemporadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarTemporadaActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow != -1){
            

            try {
                gbd.eliminarTemporada(temporades.get(selectedRow).getTemporada());
                gbd.confirmarCanvis();

                temporades.remove(selectedRow);

                omplirTaula();
            } catch (GestorBDException ex) {
                java.util.logging.Logger.getLogger(GestioTemporada.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_eliminarTemporadaActionPerformed

